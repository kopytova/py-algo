'''Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6. Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака. Объяснить полученный результат.'''

print(5 & 6) # побитовое "И"
print(5 | 6) # побитовое "ИЛИ"
print(5 ^ 6) # побитовое исключающее "ИЛИ"
print (5 << 2) # побитовый сдвиг влево на 2 символа для 5
print(5 >> 2) # поюитовый сдвиг вправо на 2 символа для 6